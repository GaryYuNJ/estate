package com.property.data;

import java.util.List;

public class FlowConfigContent {
	private List<FlowConfigContentParam> params;
	private List<FlowConfigContentFormField> formfields;
	private String taskid;
	public List<FlowConfigContentParam> getParams() {
		return params;
	}
	public void setParams(List<FlowConfigContentParam> params) {
		this.params = params;
	}
	public List<FlowConfigContentFormField> getFormfields() {
		return formfields;
	}
	public void setFormfields(List<FlowConfigContentFormField> formfields) {
		this.formfields = formfields;
	}
	public String getTaskid() {
		return taskid;
	}
	public void setTaskid(String taskid) {
		this.taskid = taskid;
	}
}
